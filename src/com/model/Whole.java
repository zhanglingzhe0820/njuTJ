package com.model;

public class Whole {

	private int id;
	private String kind;
	private String content;
	
	public void setId(int id){
		this.id=id;
	}
	
	public int getId(){
		return id;
	}
	
	public void setKind(String kind){
		this.kind=kind;
	}
	
	public String getKind(){
		return kind;
	}
	
	public void setContent(String content){
		this.content=content;
	}
	
	public String getContent(){
		return content;
	}
}
